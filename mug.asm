.org $080D
.segment "STARTUP"
.segment "INIT"
.segment "ONCE"
.segment "CODE"

   jmp start

.include "c64.inc"
.include "cbm_kernal.inc"

; PETSCII Codes
RETURN      = $0D
LOWER_CASE  = $0E
RED_CHAR    = $1C
GREEN_CHAR  = $1E
SPACE       = $20
BLACK_CHAR  = $90
CLR_CHAR    = $93

LEFT_MARGIN = 17

ZP_PTR_1    = $FB
ZP_PTR_2    = $FD

SCREEN_RAM  = $0400
SPRITE_PTRS = SCREEN_RAM | $03F8

tove_sprite_colors:
.byte 8,7,8,7,5,8,5

tove_sprite_xy:
.byte 118,75
.byte 98,95
.byte 82,107
.byte 96,135
.byte 118,121
.byte 104,131
.byte 100,131


title_str:
.byte LOWER_CASE,GREEN_CHAR,"slithy",RETURN
.res LEFT_MARGIN,SPACE
.byte BLACK_CHAR,"GAMES",RETURN,RETURN
.res LEFT_MARGIN,SPACE
.byte RED_CHAR,"PRESENTS",RETURN,RETURN,0

start:
   lda #1
   sta VIC_BORDERCOLOR
   sta VIC_BG_COLOR0
   lda #CLR_CHAR
   jsr CHROUT
   ldy #LEFT_MARGIN
   ldx #10
   clc
   jsr PLOT
   ldx #0
@title_loop:
   lda title_str,x
   beq @done_text
   jsr CHROUT
   inx
   jmp @title_loop
@done_text:
   lda #$7F
   sta VIC_SPR_MCOLOR
   sta VIC_SPR_EXP_Y
   sta VIC_SPR_ENA
   lda #15
   sta VIC_SPR_MCOLOR0
   lda #9
   sta VIC_SPR_MCOLOR1
   ldx #0
   stx VIC_SPR_HI_X
   stx VIC_SPR_EXP_X
@sprite_loop:
   txa
   ora #>((tove_sprite0 & $3E00) << 2)
   sta SPRITE_PTRS,x
   lda tove_sprite_colors,x
   sta VIC_SPR0_COLOR,x
   lda tove_sprite_xy,x
   sta VIC_SPR0_X,x
   inx
   cpx #7
   bne @sprite_loop
@coord_loop:
   lda tove_sprite_xy,x
   sta VIC_SPR0_X,x
   inx
   cpx #14
   bne @coord_loop




   rts


